public class Titles
{
    private String ja_jp;

    public String en;

    private String en_jp;

    public Titles (){}



    public String getJa_jp ()
    {
        return ja_jp;
    }

    public void setJa_jp (String ja_jp)
    {
        this.ja_jp = ja_jp;
    }

    public String getEn ()
    {
        return en;
    }

    public void setEn (String en)
    {
        this.en = en;
    }

    public String getEn_jp ()
    {
        return en_jp;
    }

    public void setEn_jp (String en_jp)
    {
        this.en_jp = en_jp;
    }
}